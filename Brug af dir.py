## dir bruges til undersøge hvilken metoder man kan bruge på listene eller tuple. 
## dir bruges typisk til find metoder som kan bruges.
#List
colours =["red" , "blue" , "green" , "white" , "black"]

print(dir(colours))

#Tuple
colours = ("red" , "blue" , "green" , "white" , "black")

print(dir(colours))

